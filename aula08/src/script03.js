// Exercício 2: Realize a operação de subtração com números inteiros informados pelo usuário e depois retorne o resultado para ele.

alert('Subtração');

let valor1 = parseInt(prompt('Informe o primeiro valor inteiro para realizar a subtração: '));

let valor2 = parseInt(prompt('Informe o segundo valor inteiro para realizar a subtração: '));

let resultadoSubtracao = valor1 - valor2;

alert('O resultado da subtração dos números é: ' + resultadoSubtracao);

