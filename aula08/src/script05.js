// Exercício 4: Realize a operação de divisão com números inteiros informados pelo usuário e depois retorne o resultado para ele.

alert('Divisão');

let valor1 = parseInt(prompt('Informe o primeiro valor inteiro para realizar a divisão: '));

let valor2 = parseInt(prompt('Informe o segundo valor inteiro para realizar a divisão: '));

let resultadoDivisao = valor1 / valor2;

alert('O resultado da divisão dos números é: ' + resultadoDivisao);