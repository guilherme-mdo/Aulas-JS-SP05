// Exercício 3: Realize a operação de multiplicação com números inteiros informados pelo usuário e depois retorne o resultado para ele.

alert('Multiplicação');

let valor1 = parseInt(prompt('Informe o primeiro valor inteiro para realizar a multiplicação: '));

let valor2 = parseInt(prompt('Informe o segundo valor inteiro para realizar a multiplicação: '));

let resultadoMultiplicacao = valor1 * valor2;

alert('O resultado da multiplicação dos números é: ' + resultadoMultiplicacao);